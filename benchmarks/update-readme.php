<?php

/**
 * Rewrite the README's end-to-end benchmark line from the live JSON that
 * `benchmarks/export-metrics.sh` produced — so the README never drifts from the
 * measured numbers. Runs on the host (no DB, no Docker): it only reads the JSON and
 * patches the marked block.
 *
 *   php benchmarks/update-readme.php
 *
 * The canonical, always-current table lives in the docs (rendered from the same JSON);
 * this keeps the README's one-line summary honest between doc visits.
 */
$root = dirname(__DIR__);
$jsonPath = "$root/docs/.vitepress/data/benchmarks.json";
$readmePath = "$root/README.md";

if (! is_file($jsonPath)) {
    fwrite(STDERR, "no $jsonPath — run benchmarks/export-metrics.sh first\n");
    exit(1);
}

$data = json_decode(file_get_contents($jsonPath), true);
if (! is_array($data) || ($data['parity'] ?? null) !== 'pass' || empty($data['macro'])) {
    fwrite(STDERR, "benchmarks.json missing/!pass/empty — refusing to update README\n");
    exit(1);
}

// Short phrase per endpoint for the one-liner.
$phrases = [
    'index_users' => 'list-100-users',
    'posts_with_author' => 'eager-load',
    'show_post' => 'show',
    'bulk_update' => 'bulk write',
];

$parts = [];
foreach ($data['macro'] as $e) {
    $phrase = $phrases[$e['key']] ?? $e['key'];
    $parts[] = sprintf('−%d%% %s', (int) round(abs($e['delta_pct'])), $phrase);
}

$line = '- **End-to-end requests (incl. SQL):** '.implode(', ', $parts).'.';
$block = "<!-- BENCH:START — generated by benchmarks/export-metrics.sh; do not edit by hand -->\n"
    .$line."\n"
    .'<!-- BENCH:END -->';

$readme = file_get_contents($readmePath);
$patched = preg_replace('/<!-- BENCH:START.*?BENCH:END -->/s', $block, $readme, 1, $count);

if ($count !== 1) {
    fwrite(STDERR, "BENCH markers not found in README.md — add the <!-- BENCH:START/END --> block\n");
    exit(1);
}

if ($patched === $readme) {
    fwrite(STDERR, "README already current.\n");
    exit(0);
}

file_put_contents($readmePath, $patched);
fwrite(STDERR, 'updated README end-to-end line: '.implode(', ', $parts)."\n");
